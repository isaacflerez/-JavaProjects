
package conditionalexample2;


public class Conditionalexample2 {

    
    public static void main(String[] args) {
        int x = 10;
        int y = 12;
        
        if(x+y<10){
            System.out.println("x + y es menor que 10");
        }
        else{
            System.out.println("x + y es mayor que 10");
        }
    }
    
}
